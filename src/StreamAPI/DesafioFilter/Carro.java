package StreamAPI.DesafioFilter;

public class Carro {

    final String modelo;
    final double forcaMotor;
    final boolean arrancadaBoa;

    public Carro(String modelo, double forcaMotor, boolean arrancadaBoa) {
        this.modelo = modelo;
        this.forcaMotor = forcaMotor;
        this.arrancadaBoa = arrancadaBoa;
    }
}
